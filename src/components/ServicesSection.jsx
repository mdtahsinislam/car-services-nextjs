import { BsArrowRight } from "react-icons/bs";
import dbConnect from "@/lib/dbConnect";
import Link from "next/link";

export default async function ServicesSection() {
  const serviceCollection = await dbConnect("test_services");
  const data = await serviceCollection.find({}).toArray();

  return (
    <div className="container mx-auto px-4 py-16 text-base-content">
      <div className="text-center">
        <p className="text-accent font-bold">Service</p>
        <h1 className="text-4xl font-bold mt-2 text-black">Our Service Area</h1>
        <p className="mt-4 max-w-2xl font-bold mx-auto text-green-500">
          The Majority Have Suffered Alteration In Some Form...
        </p>
      </div>

      
      <div className="grid grid-cols-1 md:grid-cols-2 lg:grid-cols-3 gap-8 mt-12">
        {data.map((service) => (
          <div
            key={service._id}
            className="bg-base-200 rounded-lg shadow-xl overflow-hidden"
          >
            <img
              src={service.img}
              alt={service.title}
              className="w-full h-48 object-cover"
            />
            <div className="p-6">
              <h2 className="text-xl font-semibold">{service.title}</h2>
              <div className="flex justify-between items-center mt-4">
                <p className="text-accent font-bold">
                  Price : ${service.price}
                </p>
               
              <Link
  href={`/services/${service._id}`} // service._id ঠিকভাবে ব্যবহার করো
  className="text-accent flex hover:text-accent-focus"
>
  <span>Take Services</span><BsArrowRight className="w-6 h-6" />
</Link>


                
              </div>
            </div>
          </div>
        ))}
      </div>
      <div className="text-center mt-12">
        <button className="btn btn-outline btn-accent">
          More Services
        </button>
      </div>
    </div>
  );
}
